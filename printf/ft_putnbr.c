#include "libft.h"

void	ft_putnbr(int n)
{
	char		c;
	long long	num;

	num = n;
	if (num < 0)
	{
		num *= -1;
		write(1, "-", 1);
	}
	if (num >= 10)
	{
		ft_putnbr_fd((num / 10));
	}
	c = (num % 10) + '0';
	write(1, &c, 1);
}
